function generateHistory(points: number, base: number, variance: number): number[] {
  return Array.from({ length: points }, (_, i) =>
    Math.round((base + Math.sin(i * 0.5) * variance + (Math.random() - 0.5) * variance * 0.5) * 10) / 10,
  );
}

