import type { Prompt, PromptVersion } from "../types";

function v(label: string, version: number, overrides: Partial<PromptVersion>): PromptVersion {
  return {
    version,
    label,
    systemPrompt: "",
    userTemplate: "",
    model: "gpt-4o",
    temperature: 0.3,
    tokens: 0,
    accuracy: 0,
    latency: 0,
    cost: 0,
    createdBy: "admin",
    createdAt: new Date(Date.now() - (10 - version) * 86400000 * 7).toISOString(),
    notes: "",
    ...overrides,
  };
}

