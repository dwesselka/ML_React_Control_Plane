import type {
  ModelRegistryEntry,
  ModelDeployment,
  ModelRegistryVersion,
} from "../types";

function version(ver: string, status: ModelRegistryVersion["status"] = "active", metrics?: Record<string, number>): ModelRegistryVersion {
  return {
    id: `ver-${crypto.randomUUID().slice(0, 8)}`,
    version: ver,
    status,
    metrics: metrics ?? {},
    artifactUrl: `s3://models/artifacts/${ver}`,
    createdAt: new Date(Date.now() - Math.random() * 90 * 86400000).toISOString(),
  };
}

function deployment(
  env: ModelDeployment["environment"],
  status: ModelDeployment["status"],
  provider: ModelDeployment["provider"],
  version: string,
  overrides?: Partial<ModelDeployment>,
): ModelDeployment {
  return {
    id: `dep-${crypto.randomUUID().slice(0, 8)}`,
    environment: env,
    status,
    provider,
    modelVersion: version,
    endpointUrl: `https://inference.${env}.mlplatform.io/v1/models/${overrides?.id ?? "model"}`,
    rollout: null,
    latency: { p50Ms: 120 + Math.random() * 80, p95Ms: 300 + Math.random() * 200, p99Ms: 800 + Math.random() * 400, avgMs: 150 + Math.random() * 100 },
    tokens: { totalTokens: Math.floor(500000 + Math.random() * 2000000), promptTokens: Math.floor(200000 + Math.random() * 800000), completionTokens: Math.floor(300000 + Math.random() * 1200000), totalCost: Number((2.5 + Math.random() * 15).toFixed(2)) },
    replicas: env === "production" ? 5 + Math.floor(Math.random() * 10) : env === "staging" ? 2 + Math.floor(Math.random() * 3) : 1,
    deployedAt: new Date(Date.now() - Math.random() * 30 * 86400000).toISOString(),
    updatedAt: new Date(Date.now() - Math.random() * 7 * 86400000).toISOString(),
    ...overrides,
  };
}

